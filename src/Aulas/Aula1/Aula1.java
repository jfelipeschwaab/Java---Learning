    /*
    Uma classe é um template para representar objetos em Java. Pense que uma
    classe é um blueprint, um schema para algo da vida real, em que pode
    ser representado por meio da programação, por meio das classes em
    Java

    Uma classe determina os componentes necessários e como eles interagem
    uns com os outros.

    Por exemplo, um programa designado para monitorar as grades de alunos,
    Esse programa pode incluir classes como Aluno(Student) e (Grade), para
    representar entidades da vida real dos estudantes e suas notas

    A classe Student, representando o estudante, teria campos como
    ID, Cursos e muitos outros campos de informação

    Representamos os estudantes como instâncias, ou objetos, da classe
    Student

    Isso é POO: programas são construídos utilizando objetos.



    2. Classes: Constructors
    Em java, o Constructor é um tipo especial de metodo definido com a classe,
    o Constructor é utilizado para inicializar campos de informação quando uma
    instância de classe é criada. O nome do constructor deve ser o mesmo da classe.

    Geralmente, o constructor é definido como public.
*/
